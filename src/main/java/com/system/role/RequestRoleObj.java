package com.system.role;

public class RequestRoleObj {

    private Role role;
    private String userNos;
    private String menuNos;
    private String buttonNos;

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

    public String getUserNos() {
        return userNos;
    }

    public void setUserNos(String userNos) {
        this.userNos = userNos;
    }

    public String getMenuNos() {
        return menuNos;
    }

    public void setMenuNos(String menuNos) {
        this.menuNos = menuNos;
    }

    public String getButtonNos() {
        return buttonNos;
    }

    public void setButtonNos(String buttonNos) {
        this.buttonNos = buttonNos;
    }
}
